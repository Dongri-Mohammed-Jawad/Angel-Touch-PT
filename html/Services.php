                                <h3 class="text-center color-text">Physical Therapy</h3>
                                <div class="vs-25"></div>
                            <p style="font-size:17px;"><img src="assets/img/blog/1.jpg" class="img-responsive" />Physical therapists have different titles in different countries: in many countries they are called Physical Therapists. Some countries have their own version of the word physical therapist, such as kinesiologist. They are all part of the same profession.
					   <br><br>
					   Physical therapists provide services that develop, maintain and restore people’s maximum movement and functional ability. They can help people at any stage of life, when movement and function are threatened by ageing, injury, diseases, disorders, conditions or environmental factors.
					   <br><br>
					   Physical therapists help people maximise their quality of life, looking at physical, psychological, emotional and social wellbeing. They work in the health spheres of promotion, prevention, treatment/intervention, habilitation and rehabilitation.
					   <br><br>
					   <b>Physical therapists are qualified and professionally required to:</b>
					   <br><br>
					   <ul style="font-size:17px;">
						<li style="font-size:17px;">undertake a comprehensive examination/assessment of the patient/client or needs of a client group</li>
						<li style="font-size:17px;">evaluate the findings from the examination/assessment to make clinical judgments regarding patients/clients</li>
						<li style="font-size:17px;">formulate a diagnosis, prognosis and plan</li>
						<li style="font-size:17px;">provide consultation within their expertise and determine when patients/clients need to be referred to another healthcare professional</li>
						<li style="font-size:17px;">implement a physical therapist intervention/treatment programme</li>
						<li style="font-size:17px;">determine the outcomes of any interventions/treatments</li>
						<li style="font-size:17px;">make recommendations for self-management.</li>
						
