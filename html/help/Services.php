                            <h3 class="text-center color-text">Sports Injury</h3>
                            <div class="vs-25"></div>
                            <p style="font-size:17px;"><img src="assets/img/blog/2.jpg" class="img-responsive" />Sports injuries refer to the kinds of injury that occur during sports or exercise. While it is possible to injure any part of the body when playing sports, the term sports injuries is commonly used to refer to injuries of the musculoskeletal system.
					   
					   <b>Some of the most common sports injuries include:</b>
					   <ul style="font-size:17px;">
						<li><b>Sprains </b>— tears to the ligaments that join the ends of bones together. The ankles, knees, and wrists are commonly affected by sprains.</li>
						<li><b>Strains </b>— pulls or tears of muscles or tendons (the tissues that attach the muscles to the bones)</li>
						<li>"<b>Shin splints</b>"—pain along the outside front of the lower leg, commonly seen in runners</li>
						<li><b>Achilles tendonitis or rupture of the Achilles tendon</b> — These injuries involve the large band of tissue that connects the calf muscles to the heel</li>
						<li><b>Fractures</b> of the bones</li>
						<li><b>Dislocation of joints</b></li>
					   </ul>
					   <p style="font-size:17px;">
						Acute injuries usually occur suddenly while participating in sports or exercise. They may result in sudden and severe pain, the inability to bear weight on a limb, or inability to move the affected part of the body. Chronic injuries usually result from overuse of one area of the body over a period of time. Symptoms of chronic injuries include soreness, dull aching pain, and pain during participation in physical activity.
						<br>
						<b>What happens to the tissues after an acute injury?</b>
						<br><br>
						When a runner "pulls a groin" or a tennis player "strains a tendon," the soft tissues in the area of pain are injured. Immediately after the injury, there are disrupted fibers of the affected muscle, tendon, and/or ligament. Additionally, the tiny blood vessels (capillaries) that normally supply blood and oxygen to these tissues are broken. The broken capillaries then leak varying amounts of blood and serum into the adjacent tissues. Therefore, soon after a soft tissue injury, localized swelling occurs. The injured tissues become painful and tender, both directly from the trauma to them and indirectly from the subsequent swelling. This leads to the stiffness, pain, and tenderness that so often accompany the inflammation of tendinitis, bursitis, as well as strain and sprain injuries. It should also be noted that even a bone injury (such as a fracture) is typically accompanied by injury to the nearby soft tissues.</p>
