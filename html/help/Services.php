                            <h3 class="text-center color-text">Rehabilitation</h3>
                            <div class="vs-25"></div>
                            <p style="font-size:17px;"><img src="assets/img/blog/3.jpg" class="img-responsive" />Walking your dog in a park. Dashing down a basketball court. Mowing your lawn. We usually don’t think about the great gift we have to move, bend, lift, throw or push without limitation. But an injury or illness can take away those abilities and suddenly everyday activities become extraordinary tasks.<br><br>

Physical rehabilitation is the process of restoring and regaining physical strength and function. Physical rehabilitation can be used to help treat a simple ankle sprain or to help a stroke survivor walk, talk and eat again.<br><br>

“Physical medicine and rehabilitation physicians, nurses and physical, occupational, speech and recreation therapists work as a team to help patients reduce their dependency on others and achieve the highest level of functional independence,” says Frank Hyland, vice president of rehabilitation for Good Shepherd Rehabilitation Network. “Successful rehabilitation helps you get back to doing the things you love with the people you love.”
					   <br><br>
					   <b>The Physical Rehabilitation Process</b>
					   <br>
					   Rehabilitation is a different experience for everyone, but care typically progresses in the following way:<br><br>
					   <ul style="font-size:17px;">
						<li><b>Acute or Physician Care: To Stabilize Your Condition</b> - For serious injuries and medical conditions like stroke, brain injury or multiple traumas, inpatient rehabilitation care follows an acute-care hospital stay. For injuries like sprains, strains and fractures, a visit to a physician will be the first line of care.</li>
						<li><b>Inpatient Rehabilitation: To Regain Function and Independence </b>- Patients typically receive at least three hours of physical, occupational, speech or recreational therapy each day at a rehabilitation facility such as Good Shepherd Rehabilitation Hospital-Allentown. A multidisciplinary team of physicians, therapists and nurses develop and oversee a treatment plan to meet individual goals.</li>
						<li><b>Outpatient Rehab: To Maintain and Improve Function </b>- Patients who are discharged from inpatient rehabilitation will continue therapy in an outpatient facility, such as Good Shepherd Physical Therapy. Patients with less serious conditions may be referred right to an outpatient facility for treatment. Maintaining function on an ongoing basis may include joining a medical-model fitness center, fitness class or support group.</li>
					   </ul>
					   
					   </p>
