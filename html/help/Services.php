                            <h3 class="text-center color-text">Acupuncture</h3>
                            <div class="vs-25"></div>
                            <p style="font-size:17px;"><img src="assets/img/blog/4.jpg" class="img-responsive" />Acupuncture is a form of treatment that involves inserting very thin needles through a person's skin at specific points on the body, to various depths.<br><br>
					   
					   Research suggests that it can help relieve pain, and it is used for a wide range of other complaints.<br><br>

However, according to the National Center for Complementary and Integrative Health (NCCIH), there is limited evidence for its effectiveness in areas other than pain.<br><br>

How acupuncture works scientifically remains unclear. Some people claim it works by balancing vital energy, while others believe it has a neurological effect.<br><br>

Acupuncture remains controversial among Western medical doctors and scientists.
					   </p>
					   <h3>What is Acupuncture?</h3>
					   <p style="font-size:17px;">
						An acupuncurist will insert needles into a person's body with the aim of balancing their energy.<br><br>

This, it is claimed, can help boost wellbeing and may cure some illnesses.<br><br>

Conditions it is used for include different kinds of pain, such as headaches, blood pressure problems, and whooping cough, among others.
					   </p>
					   <h3>How does it work?</h3>
					   <p style="font-size:17px;">
						Traditional Chinese medicine explains that health is the result of a harmonious balance of the complementary extremes of "yin" and "yang" of the life force known as "qi," pronounced "chi." Illness is said to be the consequence of an imbalance of the forces.<br><br>

Qi is said to flow through meridians, or pathways, in the human body. These meridiens and energy flows are accessible through 350 acupuncture points in the body.<br><br>

Inserting needles into these points with appropriate combinations is said to bring the energy flow back into proper balance.<br><br>

There is no scientific proof that the meridians or acupuncture points exist, and it is hard to prove that they either do or do not, but numerous studies suggest that acupuncture works for some conditions.<br><br>

Some experts have used neuroscience to explain acupuncture. Acupuncture points are seen as places where nerves, muscles, and connective tissue can be stimulated. The stimulation increases blood flow, while at the same time triggering the activity of the body's natural painkillers.<br><br>

It is difficult to set up investigations using proper scientific controls, because of the invasive nature of acupuncture. In a clinical study, a control group would have to undergo sham treatment, or a placebo, for results to be compared with those of genuine acupuncture.<br><br>

Some studies have concluded that acupuncture offers similar benefits to a patient as a placebo, but others have indicated that there are some real benefits.
					   </p>
