<p style="font-size:17px;">							<b>Acute &amp; Chronic Pain : </b><br>
							Acute pain usually comes on suddenly and is caused by something specific. It is sharp in quality. Acute pain usually does not last longer than six months. It goes away when there is no longer an underlying cause for the pain.Chronic pain can affect any part of the body and people of any age, including children.
							<br>
<b>Neck &amp; Back Pain</b><br>
The most common cause of acute back or neck pain is a muscle injury, in which muscle fibers stretch too far and tear. Muscle injury may be caused by overuse, such as from heavy lifting, as well as by repetitive motions that put continual stress on the back or neck muscles.<br>

<b>Frozen Shoulder (Adhesive Capsulitis)</b><br>
Adhesive capsulitis and frozen shoulder syndrome (FSS) are two terms that have been used to describe a painful and stiff shoulder. <br>

<b>Rotator Cuff Tendonitis</b><br>
Rotator cuff tendinitis, or tendonitis, affects the tendons and muscles that help move your shoulder joint. If you have tendinitis, it means that your tendons are inflamed or irritated. Rotator cuff tendinitis is also called impingement syndrome.<br>

<b>Impingement Syndrome</b><br>
impingement syndrome is a common cause of shoulder pain. It occurs when there is impingement of tendons or bursa in the shoulder from bones of the shoulder. <br>

<b>Superior Labral Tears</b><br>
A SLAP tear is a torn piece of cartilage in the inner portion of the shoulder joint. It can be painful and/or limit movement in the shoulder, especially overhead motions.<br>

<b>AC Joint Injuries</b><br>
An acromioclavicular (AC) joint injury is commonly referred to as "shoulder separation" and should not be confused with a shoulder dislocation. <br>

<b>Tennis or Golfers Elbow</b><br>
You don’t have to be Roger Federer or Phil Mickelson to have it. Even if you’ve never played a set of tennis or a round of golf, you can still suffer from these elbow injuries. Tennis Elbow and Golfer’s Elbow are both overuse injuries that are caused by any activity that requires repetitive motion of the arm and wrist. <br>

<b>Post UCL Re-construction</b><br>
Injuries to the ulnar collateral ligament (UCL) in throwing athletes frequently occurs from the repetitive valgus loading of the elbow during the throwing motion, which often results in surgical reconstruction of the UCL requiring a structured postoperative rehabilitation program. <br>

<b>Post Total Elbow Arthroplasty</b><br>
Total elbow arthroplasty has continued to evolve over time. Elbow implants may be linked or unlinked. Unlinked implants are attractive for patients with relatively well preserved bone stock and ligaments, but many favor linked implants, since they prevent instability and allow replacement for a wider spectrum of indications.<br>

<b>Pronator Teres Syndrome</b><br>
Pronator teres syndrome is a compression neuropathy of the median nerve at the elbow. <br>

<b>Post Total Hip and Knee Replacements</b><br>
The prostheses for a total hip replacement (total hip arthroplasty) can be inserted into the pelvis and femur with or without cement. The options are customized for each patient by the orthopedic surgeon.<br>

<b>Patellofemoral Pain Syndrome</b><br>
Patellofemoral pain syndrome is a broad term used to describe pain in the front of the knee and around the patella, or kneecap. It is sometimes called "runner's knee" or "jumper's knee". <br>

<b>Post Patellar Tendor &amp; Quad Tendon Repair</b><br>
When the quadriceps tendon ruptures, the patella loses its anchoring support in the thigh. Without this anchoring effect of the intact quadriceps tendon, the patella tends to move inferiorly (towards the foot).<br>

<b>Patellar Tendonitis</b><br>
Patellar tendinopathy (or as it is commonly known patellar tendonitis or tendinitis) is an overuse injury affecting your knee. It is the result of your patella tendon being overstressed. A common name for it is Jumper's Knee.<br>

<b>Post ACL Re-construction</b><br>
To gain good functional stability, repair muscle strength, reach the best possible functional level and to decrease the risk for re-injury.<br>

<b>Hip Bursitis</b><br>
Most commonly, bursitis is a noninfectious condition (aseptic bursitis) caused by inflammation that results from local soft-tissue trauma or strain injury.<br>

<b>Herniated/Bulging Disk</b><br>
“A herniated disk results when a crack in the tough outer layer of cartilage allows some of the softer inner cartilage to protrude out of the disk.A bulging disk doesn't always affect the entire perimeter of a disk, only the outer layer of tough cartilage is involved.”<br>

<b>Sciatica</b><br>
Sciatica pain is typically felt from the low back to behind the thigh and radiating down below the knee.<br>

<b>Planter Fasciitis</b><br>
Plantar fasciitis (PLAN-tur fas-e-I-tis) is one of the most common causes of heel pain. It involves inflammation of a thick band of tissue that runs across the bottom of your foot and connects your heel bone to your toes (plantar fascia).<br>
<b>Achilles Tendonitis</b><br>
Achilles tendinitis is a common condition that occurs when the large tendon that runs down the back of your lower leg becomes irritated and inflamed. <br>
<b>Peroneal Tendonitis</b><br>
Peroneal tendonitis is inflammation of the peroneal tendons which run behind the lateral malleolus or the bony bit on the outside of the ankle causing and swelling on the outer ankle.<br>

<b>Ankle Sprain</b><br>
An ankle sprain is an injury to the tough bands of tissue (ligaments) that surround and connect the bones of the leg to the foot. <br>

<b>Carpal Tunnel Syndrome</b><br>
Carpal tunnel syndrome is a common condition that causes pain, numbness, and tingling in the hand and arm. The condition occurs when one of the major nerves to the hand — the median nerve — is squeezed or compressed as it travels through the wrist. <br>

<b>Post-Surgical Rehab</b><br>
Post surgery, one needs to go through various exercises to optimize results and come back to their best self. <br>

<b>Weight Management</b><br>
Physiotherapy provides the physical activity component of multidisciplinary weight management services.<br>
<b>Sports injury</b><br>
Sports injuries are injuries that occur in athletic activities or exercising.<br>

<b>Spinal &amp; Joint Dysfunction</b><br>
Sacroiliac joint dysfunction is improper movement of the joints at the bottom of the spine that connect the sacrum to the pelvis. It can result in pain in the low back and legs, or inflammation of the joints known as sacroiliitis.<br>
<b>Balance Training</b><br>
Balance training involves doing exercises that strengthen the muscles that help keep you upright, including your legs and core. These kinds of exercises can improve stability and help prevent falls.<br>

<b>Headaches &amp; Dizziness</b><br>
Dizziness that accompanies a headache could be a sign of a migraine, but it can also be related to others factors. <br>

<b>General De-conditioning</b><br>
Deconditioning is a complex process of physiological change following a period of inactivity, bedrest or sedentary lifestyle. It results in functional losses in such areas as mental status, degree of continence and ability to accomplish activities of daily living. It is frequently associated with hospitalization in the elderly. 

